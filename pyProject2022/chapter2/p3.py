'''
元组：用圆括号括起来的元素集合（x,y,z),元组的值不可能变
'''
a=(1,2,3)
print(a)
'''
字典：有一组值（键码：值）构成一个元素，将这些元素用大括号括起来，元素之间用逗号分割
'''
x={"x":10,"y":20,"z":30}#字典
print(x)
x['a']=40 #加入一个元素及其值
print(x)
'''
字典通常和格式化字符串混合使用
商品名称mc，商品价格jg，商品产地cd
'红富士苹果',20,"四川"
'''
temp={'mc':'红富士苹果','jg':20,'cd':'四川'}
format="{mc},{jg},{cd}".format(**temp)#**代表引用字典的所有参数
print(format)